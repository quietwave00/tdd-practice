package model;

public class CalculatorEngine {
    public int plus(final int target,final int number) {
        return target + number;
    }

    public int minus(int target, int number) {
        return target - number;
    }

    public int times(int target, int number) {
        return target * number;
    }

    public int divide(int target, int number) {
        return target / number;
    }
}
